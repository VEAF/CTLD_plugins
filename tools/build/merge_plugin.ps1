# merge_plugin.ps1 - Build one plugin scene into a single loadable .lua.
# Output: dist/<plugin>.lua in UTF-8 WITHOUT BOM (required by the DCS Lua engine).
# The mission-maker loads this file from a mission-start trigger AFTER CTLD.
#
# Usage:
#   powershell -ExecutionPolicy Bypass -File tools/build/merge_plugin.ps1 -Plugin metal-farp
#   powershell -ExecutionPolicy Bypass -File tools/build/merge_plugin.ps1 -All
#
# Source order: plugins/<plugin>/src/listToMerge.txt if present (one path per line, relative to
# that src/ dir), otherwise every *.lua in src/ sorted by name.
[CmdletBinding()]
param(
    [string]$Plugin,
    [switch]$All
)

$ErrorActionPreference = "Stop"
$scriptDir = Split-Path -Parent $MyInvocation.MyCommand.Path
$repoRoot  = Resolve-Path (Join-Path $scriptDir "..\..")
$pluginsDir = Join-Path $repoRoot "plugins"
$distDir   = Join-Path $repoRoot "dist"
$utf8NoBOM = [System.Text.UTF8Encoding]::new($false)

if (-not (Test-Path $distDir)) { New-Item -ItemType Directory -Path $distDir | Out-Null }

function Build-One($name) {
    $srcDir = Join-Path $pluginsDir (Join-Path $name "src")
    if (-not (Test-Path $srcDir)) { throw "No src/ for plugin '$name' ($srcDir)" }

    $listFile = Join-Path $srcDir "listToMerge.txt"
    if (Test-Path $listFile) {
        $files = Get-Content $listFile |
            Where-Object { $_ -notmatch '^\s*(--|$)' } |
            ForEach-Object { Join-Path $srcDir $_.Trim() }
    } else {
        $files = Get-ChildItem -Path $srcDir -Filter *.lua | Sort-Object Name | ForEach-Object { $_.FullName }
    }
    if (-not $files) { throw "No Lua source files for plugin '$name'" }

    # Extract metadata from the merged source for the banner (best-effort regex).
    $joined      = ($files | ForEach-Object { [System.IO.File]::ReadAllText($_) }) -join "`n"
    $requiresCtld = if ($joined -match 'requiresCtld\s*=\s*"([^"]+)"') { $Matches[1] } else { "unknown" }
    $requiresMod  = if ($joined -match 'requiresMod\s*=\s*"([^"]+)"')  { $Matches[1] } else { "" }
    $buildDate    = (Get-Date).ToString("yyyy-MM-dd")

    $sb = [System.Text.StringBuilder]::new()
    $null = $sb.AppendLine('---@diagnostic disable')
    $null = $sb.AppendLine('--[[')
    $null = $sb.AppendLine("    CTLD plugin scene: $name")
    $null = $sb.AppendLine("    Requires CTLD >= $requiresCtld")
    if ($requiresMod -ne "") { $null = $sb.AppendLine("    Requires DCS mod: $requiresMod") }
    $null = $sb.AppendLine("    Built  : $buildDate")
    $null = $sb.AppendLine("    Source : https://github.com/VEAF/CTLD_plugins")
    $null = $sb.AppendLine("    Load this file from a MISSION START trigger AFTER CTLD.")
    $null = $sb.AppendLine("    DO NOT EDIT - generated by tools/build/merge_plugin.ps1")
    $null = $sb.AppendLine(']]')
    $null = $sb.AppendLine('')

    foreach ($f in $files) {
        $rel = $f.Substring($srcDir.Length).TrimStart('\','/')
        $null = $sb.AppendLine("-- ==== $rel ====")
        $null = $sb.AppendLine([System.IO.File]::ReadAllText($f))
    }

    $out = Join-Path $distDir "$name.lua"
    [System.IO.File]::WriteAllText($out, $sb.ToString(), $utf8NoBOM)
    $size = (Get-Item $out).Length
    Write-Host "Built $name -> dist/$name.lua ($size bytes, requiresCtld=$requiresCtld)"

    $bytes = [System.IO.File]::ReadAllBytes($out)
    if ($bytes[0] -eq 0xEF -and $bytes[1] -eq 0xBB -and $bytes[2] -eq 0xBF) {
        throw "BOM detected in $out"
    }
}

if ($All) {
    Get-ChildItem -Path $pluginsDir -Directory | ForEach-Object { Build-One $_.Name }
} elseif ($Plugin) {
    Build-One $Plugin
} else {
    throw "Specify -Plugin <name> or -All"
}
